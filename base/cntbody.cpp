#include <cx/base/cntbody.h>


//-------------------------------------------------------------------------
// CxCountedBody::CxCountedBody
//
//-------------------------------------------------------------------------
CxCountedBody::CxCountedBody()
: _count(0) 
{
}

//-------------------------------------------------------------------------
// CxCountedBody::~CxCountedBody
//
//-------------------------------------------------------------------------
CxCountedBody::~CxCountedBody() 
{
}

//-------------------------------------------------------------------------
// CxCountedBody::incCount
//
//-------------------------------------------------------------------------
void 
CxCountedBody::incCount() 
{
    _count++;
}

//-------------------------------------------------------------------------
// CxCountedBody::decCount
//
//-------------------------------------------------------------------------
void 
CxCountedBody::decCount() 
{
    _count--;
}

//-------------------------------------------------------------------------
// CxCountedBody::count
//
//-------------------------------------------------------------------------
int 
CxCountedBody::count() 
{
    return _count;
}
